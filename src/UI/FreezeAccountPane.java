    private void freezeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_freezeBtnActionPerformed
        if (jCheckBox1.isSelected()) {
            root.SqlConnector x = new root.SqlConnector();
            
            try {
                int hit = x.connection
                        .createStatement()
                        .executeUpdate("UPDATE users SET isFreezed = 1 WHERE accountNumber = "
                                + root.OBI.envVars.get("accountNumber"));
                if (hit == 1) {
                    javax.swing.SwingUtilities.invokeLater(() -> {
                        javax.swing.JFrame r = new Dialog("Your Account is Freezed.");
                        r.setVisible(true);
                        r.setLocationRelativeTo(null);
                    });
                    
                }
                
                x.connection.close();
            }
            catch (java.sql.SQLException err) {
                System.out.println("ERROR: " + err);
            }
        }
    }//GEN-LAST:event_freezeBtnActionPerformed


