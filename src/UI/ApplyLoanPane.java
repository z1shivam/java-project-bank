    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String LoanId = UUID.randomUUID().toString();
        float amount = Float.parseFloat(amountTf.getText());
        float rate = Float.parseFloat(rateTf.getText());
        float time = Float.parseFloat(timeTf.getText());
        float si = ((amount * rate * time) / 100);
        float pAmount = si + amount;

        loanAmountLb.setText("Total Loan Amount: Rs. " + pAmount);

        javax.swing.SwingUtilities.invokeLater(() -> {
            javax.swing.JFrame r = new Dialog("You have Successfully applied for Loan.");
            r.setVisible(true);
            r.setTitle("Loan Application");
            r.setLocationRelativeTo(null);
        });

        root.SqlConnector x = new root.SqlConnector();

        try {
            String insertQuery = "INSERT INTO loan (accountNumber, loanId, amount, rate, time, si, loanAmount, createdAt) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            java.sql.PreparedStatement stmt = x.connection.prepareStatement(insertQuery);

            stmt.setString(1, root.OBI.envVars.get("accountNumber"));
            stmt.setString(2, LoanId);
            stmt.setDouble(3, amount);
            stmt.setDouble(4, rate);
            stmt.setDouble(5, time);
            stmt.setDouble(6, si);
            stmt.setDouble(7, pAmount);
            stmt.setTimestamp(8, new java.sql.Timestamp(System.currentTimeMillis()));

            stmt.executeUpdate();

        } catch (java.sql.SQLException err) {
            System.out.println("ERROR: " + err);
        }

    }//GEN-LAST:event_jButton1ActionPerformed


